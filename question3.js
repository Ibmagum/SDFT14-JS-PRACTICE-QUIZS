let score = 85;  // Change this value to test different scores

if (score >= 90 && score <= 100) {
  console.log("A");
} else if (score >= 80 && score <= 89) {
  console.log("B");
} else if (score >= 70 && score <= 79) {
  console.log("C");
} else if (score < 70) {
  console.log("Fail");
} else {
  console.log("Invalid score");
}
